"""
make_tsla_figure_table3.py -- TSLA figure generated by the SAME code that
produced Table III of the paper, so the figure and the table agree.

PROVENANCE (important -- read before using this figure)
  Table III was produced by the first committed revision of
  eval/eval_recommendation.py (git 41ff9e2), whose signal function is:

      def simple_momentum_signal(closes, idx):
          ma5  = closes[idx-5:idx].mean()
          ma20 = closes[idx-20:idx].mean()
          buy if ma5 > ma20*1.01, sell if ma5 < ma20*0.99, else hold

  That is a 5/20-day moving-average crossover, NOT the FINDEC agent pipeline.
  The script's own docstring says so ("a simple momentum proxy signal, not the
  full live-news FinanceCrew pipeline") and its main() prints the same warning
  at runtime. The backtest also:
    - takes SHORT positions (exposure = -position_pct on a sell signal),
    - applies NO transaction cost inside the equity loop,
    - uses a FIXED position size per risk profile, not confidence scaling.

  The paper currently describes Table III as "the medium-risk-profile portfolio
  backtest -- transaction costs included, positions scaled by confidence". That
  description does not match this code. See docs/PAPER_EDITS_TODAY.md.

  One thing this backtest gets RIGHT: the signal at index idx-1 is applied to
  the return from idx-1 to idx, so it is correctly lagged and contains no
  look-ahead. Table III is not affected by the defect found in the later
  revision of this file.

Output: eval/results/tsla_table3_consistent.pdf / .png
"""

from __future__ import annotations

import numpy as np
import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt

from data_fetch import fetch_history

RISK_POSITION_PCT = {"low": 0.06, "medium": 0.10, "high": 0.16}
TRADING_DAYS = 252


def simple_momentum_signal(closes: np.ndarray, idx: int) -> str:
    """Verbatim reimplementation of the Table III signal (git 41ff9e2)."""
    if idx < 20:
        return "hold"
    ma5 = closes[idx - 5:idx].mean()
    ma20 = closes[idx - 20:idx].mean()
    if ma5 > ma20 * 1.01:
        return "buy"
    if ma5 < ma20 * 0.99:
        return "sell"
    return "hold"


def equity_curve(closes: np.ndarray, risk_profile: str) -> tuple[np.ndarray, np.ndarray]:
    position_pct = RISK_POSITION_PCT[risk_profile]
    equity = [1.0]
    exposures = []
    for idx in range(1, len(closes)):
        signal = simple_momentum_signal(closes, idx - 1)
        daily_return = (closes[idx] - closes[idx - 1]) / closes[idx - 1]
        exposure = position_pct if signal == "buy" else (-position_pct if signal == "sell" else 0.0)
        exposures.append(exposure)
        equity.append(equity[-1] * (1 + daily_return * exposure))
    return np.array(equity), np.array(exposures)


def metrics(equity: np.ndarray) -> tuple[float, float, float]:
    r = np.diff(equity) / equity[:-1]
    ann = (equity[-1] ** (TRADING_DAYS / len(equity)) - 1) * 100
    sharpe = 0.0 if r.std() == 0 else float(np.sqrt(TRADING_DAYS) * r.mean() / r.std())
    rm = np.maximum.accumulate(equity)
    mdd = float(((equity - rm) / rm).min() * 100)
    return float(ann), sharpe, mdd


def main() -> None:
    closes = fetch_history("TSLA", "5y")["Close"].to_numpy()
    eq_s, exposures = equity_curve(closes, "medium")
    eq_b = closes / closes[0]
    n = min(eq_s.size, eq_b.size)
    eq_s, eq_b = eq_s[:n], eq_b[:n]
    days = np.arange(n)

    def dd(eq):
        rm = np.maximum.accumulate(eq)
        return (eq - rm) / rm * 100

    fig, (ax1, ax2) = plt.subplots(
        2, 1, figsize=(3.5, 3.3), sharex=True,
        gridspec_kw={"height_ratios": [2.0, 1.0], "hspace": 0.12})

    ax1.plot(days, eq_b, lw=1.1, color="#8A8A8A", label="Buy and hold")
    ax1.plot(days, eq_s, lw=1.4, color="#1F4E9C", label="Risk-managed (medium)")
    ax1.axhline(1.0, color="#BBBBBB", lw=0.5, ls="-")
    ax1.set_ylabel("Growth of $1", fontsize=7)
    ax1.legend(fontsize=5.8, loc="upper left", frameon=False)
    ax1.tick_params(labelsize=6)
    ax1.grid(alpha=0.22, lw=0.4)

    ax2.fill_between(days, dd(eq_b), 0, color="#8A8A8A", alpha=0.38, lw=0,
                     label="Buy and hold")
    ax2.fill_between(days, dd(eq_s), 0, color="#1F4E9C", alpha=0.6, lw=0,
                     label="Risk-managed")
    ax2.set_ylabel("Drawdown (%)", fontsize=7)
    ax2.set_xlabel("Trading day", fontsize=7)
    ax2.tick_params(labelsize=6)
    ax2.grid(alpha=0.22, lw=0.4)
    ax2.legend(fontsize=5.5, loc="lower left", frameon=False)

    for ax in (ax1, ax2):
        for sp in ax.spines.values():
            sp.set_linewidth(0.6)

    fig.subplots_adjust(left=0.165, right=0.985, top=0.98, bottom=0.135)
    from pathlib import Path
    out = Path(__file__).resolve().parent / "results"
    out.mkdir(exist_ok=True)
    for ext in ("pdf", "png"):
        fig.savefig(out / f"tsla_table3_consistent.{ext}", dpi=400, bbox_inches="tight")
    print(f"wrote {out / 'tsla_table3_consistent.pdf'}")

    a_s, sh_s, dd_s = metrics(eq_s)
    a_b, sh_b, dd_b = metrics(eq_b)
    print("\nFigure metrics (must match Table III row TSLA):")
    print(f"  Risk-managed  ann={a_s:+6.2f}%  Sharpe={sh_s:+.2f}  MaxDD={dd_s:+7.2f}%")
    print(f"  Buy and hold  ann={a_b:+6.2f}%  Sharpe={sh_b:+.2f}  MaxDD={dd_b:+7.2f}%")
    print(f"  Paper Table III TSLA: model 3.30 / 0.64 ; B&H 13.00 / 0.50")


if __name__ == "__main__":
    main()
